#Trình bày một số cách nhập dữ liệu từ bàn phím:
#1. Nhập chuỗi, cú pháp: name = input(), ghi chú: luôn trả về kiểu String
#2. Nhập số nguyên, cú pháp: age = int(input()), ghi chú: dùng int() để ép kiểu
#3. Nhập số thực, cú pháp: x = float(input()), ghi chú: dùng float() để ép kiểu
#4. Nhập nhiều giá trị, cú pháp: a, b = input().split(), ghi chú: Trả về chuỗi, cần ép kiểu nếu cần
#5. Nhập danh sách số, cú pháp: lst = list(map(int, input().split())), ghi chú: Dùng map() và list()
